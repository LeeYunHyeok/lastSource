package com.org.iopts.dto;

public class Pi_DatetypeVO {
	private String datatype_id;
	private String datatype_label;
	private String datatype_owner;
	private String datatype_version;
	
	public String getDatatype_id() {
		return datatype_id;
	}
	public void setDatatype_id(String datatype_id) {
		this.datatype_id = datatype_id;
	}
	public String getDatatype_label() {
		return datatype_label;
	}
	public void setDatatype_label(String datatype_label) {
		this.datatype_label = datatype_label;
	}
	public String getDatatype_owner() {
		return datatype_owner;
	}
	public void setDatatype_owner(String datatype_owner) {
		this.datatype_owner = datatype_owner;
	}
	public String getDatatype_version() {
		return datatype_version;
	}
	public void setDatatype_version(String datatype_version) {
		this.datatype_version = datatype_version;
	}
	
}
